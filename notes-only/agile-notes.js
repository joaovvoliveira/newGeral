`Vantagens de metodologias ageis:

${Scrum}

Divide o trabalho em Iteracoes, chamadas Sprints, de 2 a 4 semanas.
Tem papeis definidos de cada pessoa envolvida.
Possui reunioes especificas como Daily, Sprint Planning, Sprint Review e Retrospectiva.

${Kanban}

Oferece um fluxo continuo, sem iteracoes fixas.
Nao impoe papeis fixos, ha maior flexibilidade.
Foca na visualizacao do fluxo de trabalho por meio de um quadro Kanban, representando os estagios do processo.`


